// 攻撃タイプ atk が、防御タイプ配列 defTypes に与える合計倍率
export function typeMultiplier(atk, defTypes) {
  let m = 1;
  for (const d of defTypes) {
    const row = CHART[atk];
    if (row && row[d] !== undefined) m *= row[d];
  }
  return m;
}
